#include <fstream>
#include <vector>
#include <string>

void saveBoardToFile(const std::string& filename, const std::vector<std::vector<int>>& board) {
    std::ofstream outfile(filename);

    for (const auto& row : board) {
        for (const auto& cell : row) {
            outfile << cell << ' ';
        }
        outfile << '\n';
    }
}

